def load_model_with_weights(model_constructor, weights_path, model_name, num_classes=5):
    """Load trained model with weights"""
    print(f"\n📦 Loading {model_name} from: {weights_path}")

    if not os.path.exists(weights_path):
        print(f"❌ File not found: {weights_path}")
        return None

    try:
        model = model_constructor(pretrained=False, num_classes=num_classes)
        state_dict = torch.load(weights_path, map_location='cpu')

        # Remove 'module.' prefix if present
        new_state_dict = {}
        for k, v in state_dict.items():
            name = k.replace('module.', '')
            new_state_dict[name] = v

        model.load_state_dict(new_state_dict, strict=False)

        if torch.cuda.is_available():
            model = model.cuda()

        model.eval()
        print(f"✅ {model_name} loaded successfully")
        return model

    except Exception as e:
        print(f"❌ Error loading {model_name}: {e}")
        return None

