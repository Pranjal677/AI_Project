def predict_with_multiple_filters(image_np, model, conf_threshold=0.85, min_lesion=500):
    """Multiple filters to reduce false positives"""

    input_tensor = torch.tensor(image_np, dtype=torch.float32)
    input_tensor = input_tensor.permute(2, 0, 1) / 255.0
    input_tensor = input_tensor.unsqueeze(0)

    if torch.cuda.is_available():
        input_tensor = input_tensor.cuda()

    model.eval()
    with torch.no_grad():
        output = model(input_tensor)
        if isinstance(output, tuple):
            output = output[0]

        probs = torch.softmax(output, dim=1)
        pred_mask = torch.argmax(output, dim=1)
        confidence = torch.max(probs, dim=1)[0]

        mask = pred_mask.squeeze().cpu().numpy().astype(np.uint8)
        conf = confidence.squeeze().cpu().numpy()

        # Filter 1: Confidence threshold
        mask_filtered = np.where((mask == 1) & (conf >= conf_threshold), 1, 0)

        # Filter 2: Remove small isolated regions (noise)
        mask_filtered = ndimage.binary_opening(mask_filtered, structure=np.ones((3,3))).astype(np.uint8)
        mask_filtered = ndimage.binary_closing(mask_filtered, structure=np.ones((3,3))).astype(np.uint8)

        # Filter 3: Minimum lesion size
        labeled, num_features = ndimage.label(mask_filtered)
        for i in range(1, num_features + 1):
            if np.sum(labeled == i) < min_lesion:
                mask_filtered[labeled == i] = 0

    return mask_filtered, conf, mask

