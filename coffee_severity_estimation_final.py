def predict_with_confidence(image_tensor, model, confidence_threshold=0.7):
    """
    Only classify pixels as diseased if model confidence > threshold
    """
    model.eval()
    with torch.no_grad():
        output = model(image_tensor)
        if isinstance(output, tuple):
            output = output[0]

        # Get probabilities using softmax
        probs = torch.softmax(output, dim=1)

        # Get predicted class and confidence
        pred_mask = torch.argmax(output, dim=1)
        confidence = torch.max(probs, dim=1)[0]

        # Apply confidence threshold
        mask = pred_mask.squeeze().cpu().numpy().astype(np.uint8)
        conf = confidence.squeeze().cpu().numpy()

        # Only keep disease pixels with high confidence
        mask_filtered = np.where((mask == 1) & (conf >= confidence_threshold), 1, 0)

    return mask_filtered, conf

