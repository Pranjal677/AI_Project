def preprocess_image(image_path, target_size=(224, 224)):
    """Preprocess image for classification"""
    img = Image.open(image_path).convert("RGB")
    img = img.resize(target_size)

    img_array = np.array(img).astype(np.float32) / 255.0
    img_tensor = torch.from_numpy(img_array).permute(2, 0, 1)

    # ImageNet normalization
    mean = torch.tensor([0.485, 0.456, 0.406]).view(3, 1, 1)
    std = torch.tensor([0.229, 0.224, 0.225]).view(3, 1, 1)
    img_tensor = (img_tensor - mean) / std

    return img_tensor.unsqueeze(0)

def predict_severity(model, image_path):
    """Predict severity class for a single image"""
    img_tensor = preprocess_image(image_path)

    if torch.cuda.is_available():
        img_tensor = img_tensor.cuda()

    with torch.no_grad():
        output = model(img_tensor)

        # Handle multi-task output
        if isinstance(output, tuple):
            output = output[1] if len(output) >= 2 else output[0]

        probabilities = torch.softmax(output, dim=1)
        predicted_class = torch.argmax(probabilities, dim=1).item()
        confidence = probabilities[0, predicted_class].item()
        prob_distribution = probabilities.cpu().numpy()[0]

    return {
        'severity_class': predicted_class,
        'severity_label': SEVERITY_CLASSES[predicted_class],
        'confidence': confidence,
        'probabilities': prob_distribution
    }

